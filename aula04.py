#estrutura de repetição while
cont = 1
print("1ª Estrutura de repetição")
while cont<=45:
    print(cont, end=" ")
    cont+=1
print("\nValor final do cont",cont)

print("\n2ª Estrutura de repetição")
while cont>0:
    print(cont,end=" ")
    cont-=1
print("\n3ª Estrutura de repetição")
for i in range(0,10):
    print(i,end=" ")
print("\n***************")
a = range(1,10)
